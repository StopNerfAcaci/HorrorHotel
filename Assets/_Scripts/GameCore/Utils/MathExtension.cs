using UnityEngine;

namespace Utils.Extensions
{
    public static class MathExtension
    {
        public static float ClampAngle(this float lfAngle, float lfMin, float lfMax)
        {
            if (lfAngle < -360f) lfAngle += 360f;
            if (lfAngle > 360f) lfAngle -= 360f;
            return Mathf.Clamp(lfAngle, lfMin, lfMax);
        }
    }
}